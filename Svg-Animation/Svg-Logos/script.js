// ==================== Reload Function (Use Any One) ====================

// In Jquery, we can use the following code to reload the SVG file.

$("div.svgContent i#reload").click(function () {
    $(this)
        .parent()
        .find("object")
        .each(function (index, el) {
            $(el).attr("data", $(el).attr("data"));
        });
});

// In Javascript, we can use the following code to reload the SVG file.

let svgFrameReload = document.querySelectorAll("div.svgContent i#reload");

for (let i = 0; i < svgFrameReload.length; i++) {
    svgFrameReload[i].addEventListener("click", function () {
        this.parentElement
            .querySelector("object")
            .setAttribute(
                "data",
                this.parentElement.querySelector("object").getAttribute("data")
            );
    });
}
